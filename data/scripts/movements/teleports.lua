local bossesEntrances = MoveEvent()
function bossesEntrances.onStepIn(creature, item, position, fromPosition)
    local player = creature:getPlayer()
    if not player then return false end
    for value in pairs(config) do
        if player:getLevel() < config[value].level and Position(config[value].position) == player:getPosition() then
            Spdlog.warn(config[value].level)
            player:sendTextMessage(MESSAGE_EVENT_ADVANCE, string.format(
                                       "You need at least level %s to enter.",
                                       config[value].level))
            player:teleportTo(fromPosition, true)
            player:getPosition():sendMagicEffect(CONST_ME_TELEPORT)
            return true
        end
        if Position(config[value].position) == player:getPosition() then
            player:teleportTo(Position(config[value].destination))
            player:getPosition():sendMagicEffect(CONST_ME_TELEPORT)
            return true
        end
    end
end

bossesEntrances:type("stepin")
for value in pairs(config) do bossesEntrances:position(config[value].position) end
bossesEntrances:register()
